void GPIOD_init(void)
{
		RCGCGPIO|=(1<<1);	                   /* Enable Clock for PORT B */
	  GPIOHBCTL|=(1<<1);                   /* Enable GPIO to use AHB Bus */
		GPIODIR_PORT_B|=(OUT<<PIN6);         /* Set Direction for PORT B PIN 6 as OUTPUT */
	  GPIODR2R_PORT_B|=(1<<PIN6);          /* Drive PORT B PIN with 2-mA */
	  //GPIOODR_PORT_B &=~(1<<PIN6);       /* Clear Open Drain Pad for PORT B PIN 6 */
		GPIOPDR_PORT_B|=(1<<PIN6);           /* Set Pull-down Pad for PORT B PIN 6*/
		GPIODEN_PORT_B|=(1<<PIN6);           /* Enable Digital Input/Output for PORT B PIN 6 */
}

void GPIOD_write(uint8_t value)
{
		if(value==HIGH)
		{
		  PB6=0x40;
		}
		else if(value==LOW)
		{
			PB6=0;
		}
}

